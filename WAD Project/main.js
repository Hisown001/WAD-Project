//regular array
let students =['davis','michelle','cedra'];

//pop up
students.pop();
console.log(students);

//push
students.push('noah','solomon');
console.log(students);

//shift
students.shift();
console.log(students);

//unshift
students.unshift('john','trevor');
console.log(students);

//slice
students.slice('mark','william');
console.log(students);

//associative arrays
let items={
    "fruit":'apple',
    "vegetable":'nakati',
    "other item": 'mango',
};
console.log(items.fruit);